namespace BaldejFramework.Render
{
    public class VertData
    {
        public float X;
        public float Y;
        public float Z;

        public VertData(float x, float y, float z)
        {
            X = x;
            Y = y;
            Z = z;
        }

        public VertData(float x, float y)
        {
            X = x;
            Y = y;
        }
    }
}
